// Better hash — spreads images evenly, avoids adjacent repeats
function hash(str: string): number {
  let h = 5381;
  for (let i = 0; i < str.length; i++) h = (h * 33) ^ str.charCodeAt(i);
  return Math.abs(h);
}

export function getProductImage(
  imageUrl: string | undefined | null,
  seed: number | string = 0,
  category?: string
): string {
  if (imageUrl && imageUrl.startsWith('http') && !imageUrl.includes('placeholder') && !imageUrl.includes('via.placeholder')) {
    return imageUrl;
  }
  const cat = (category || '').toLowerCase();
  let pool = FASHION_IMAGES.general;
  if (cat.includes('dress') || cat.includes('gown') || cat.includes('skirt')) pool = FASHION_IMAGES.dress;
  else if (cat.includes('shoe') || cat.includes('sneaker') || cat.includes('heel') || cat.includes('boot') || cat.includes('sandal')) pool = FASHION_IMAGES.shoes;
  else if (cat.includes('bag') || cat.includes('purse') || cat.includes('handbag') || cat.includes('tote')) pool = FASHION_IMAGES.bags;
  else if (cat.includes('street')) pool = FASHION_IMAGES.streetwear;
  else if (cat.includes('luxury')) pool = FASHION_IMAGES.luxury;
  else if (cat.includes('access') || cat.includes('jewel') || cat.includes('necklace') || cat.includes('ring') || cat.includes('chain')) pool = FASHION_IMAGES.accessories;
  else if (cat.includes('watch')) pool = FASHION_IMAGES.watches;
  else if (cat.includes('native') || cat.includes('agbada') || cat.includes('ankara') || cat.includes('aso') || cat.includes('buba')) pool = FASHION_IMAGES.nativewear;
  else if (cat.includes('hoodie') || cat.includes('sweat') || cat.includes('jacket') || cat.includes('coat')) pool = FASHION_IMAGES.hoodies;

  const key = typeof seed === 'string' ? seed : String(seed);
  const idx = hash(key + cat) % pool.length;
  return pool[idx];
}

