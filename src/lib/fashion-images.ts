// Deterministic image picker — same product always gets same image
export function getProductImage(
  imageUrl: string | undefined | null,
  index: number = 0,
  category?: string
): string {
  if (imageUrl && imageUrl.startsWith('http') && !imageUrl.includes('placeholder')) {
    return imageUrl;
  }
  const cat = (category || '').toLowerCase();
  let pool = FASHION_IMAGES.general;
  if (cat.includes('dress')) pool = FASHION_IMAGES.dress;
  else if (cat.includes('shoe') || cat.includes('sneaker') || cat.includes('heel') || cat.includes('boot')) pool = FASHION_IMAGES.shoes;
  else if (cat.includes('bag') || cat.includes('purse') || cat.includes('handbag')) pool = FASHION_IMAGES.bags;
  else if (cat.includes('street')) pool = FASHION_IMAGES.streetwear;
  else if (cat.includes('luxury')) pool = FASHION_IMAGES.luxury;
  else if (cat.includes('access') || cat.includes('jewel') || cat.includes('necklace') || cat.includes('ring')) pool = FASHION_IMAGES.accessories;
  else if (cat.includes('watch')) pool = FASHION_IMAGES.watches;
  else if (cat.includes('native') || cat.includes('agbada') || cat.includes('ankara') || cat.includes('aso')) pool = FASHION_IMAGES.nativewear;
  else if (cat.includes('hoodie') || cat.includes('sweat')) pool = FASHION_IMAGES.hoodies;
  return pool[Math.abs(index) % pool.length];
}

